export function generateVibeSlug(): string {
  const numberDictionary = NumberDictionary.generate({
    min: 1000,
    max: 9999,
  });

  return uniqueNamesGenerator({
    dictionaries: [
      [...musicians, ...cities, ...adjectives],
      [...nouns, ...tech, ...legends],
      numberDictionary,
    ],
    length: 3,
    separator: "-",
  });
}
